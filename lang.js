// Language management
let currentLang = localStorage.getItem('uttarkhan_lang') || 'bn';

// Initialize on page load
document.addEventListener('DOMContentLoaded', function() {
    initLanguage();
    attachLanguageListeners();
});

// Initialize language
function initLanguage() {
    updateContent(currentLang);
    updateActiveButton(currentLang);
}

// Update all content based on language
function updateContent(lang) {
    const elements = document.querySelectorAll('[data-key]');
    
    elements.forEach(element => {
        const key = element.getAttribute('data-key');
        
        if (content[lang] && content[lang][key]) {
            // Handle different element types
            if (element.tagName === 'INPUT' || element.tagName === 'TEXTAREA') {
                element.placeholder = content[lang][key];
            } else if (element.tagName === 'TITLE') {
                element.textContent = content[lang][key];
            } else {
                element.innerHTML = content[lang][key];
            }
        }
    });
    
    // Update HTML lang attribute
    document.documentElement.lang = lang;
}

// Update active button state
function updateActiveButton(lang) {
    const buttons = document.querySelectorAll('.lang-btn');
    
    buttons.forEach(btn => {
        if (btn.getAttribute('data-lang') === lang) {
            btn.classList.add('active');
        } else {
            btn.classList.remove('active');
        }
    });
}

// Attach event listeners to language buttons
function attachLanguageListeners() {
    const buttons = document.querySelectorAll('.lang-btn');
    
    buttons.forEach(btn => {
        btn.addEventListener('click', function() {
            const selectedLang = this.getAttribute('data-lang');
            
            if (selectedLang !== currentLang) {
                currentLang = selectedLang;
                localStorage.setItem('uttarkhan_lang', selectedLang);
                updateContent(selectedLang);
                updateActiveButton(selectedLang);
            }
        });
    });
}

// Smooth scroll for anchor links
document.querySelectorAll('a[href^="#"]').forEach(anchor => {
    anchor.addEventListener('click', function (e) {
        e.preventDefault();
        const target = document.querySelector(this.getAttribute('href'));
        if (target) {
            target.scrollIntoView({
                behavior: 'smooth',
                block: 'start'
            });
        }
    });
});